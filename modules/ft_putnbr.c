#include "modules.h"

void	ft_putnbr(int n)
{
	long	nb;

	nb = (long) n;
	if (nb < 0x0)
	{
		nb = -nb;
		write(0x1, "-", 0x1);
	}
	if (nb > 9)
		ft_putnbr(nb / 10);
	write(0x1, &"0123456789"[nb % 10], 0x1);
}
